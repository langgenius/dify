import { mkdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { importSvgCollections } from 'iconify-import-svg'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const packageDir = path.resolve(__dirname, '..')
const generatedDir = path.resolve(packageDir, 'src/generated')

const parseColorOptions = {
  fallback: () => 'currentColor',
}
const svgOptimizeConfig = {
  cleanupSVG: true,
  deOptimisePaths: true,
  runSVGO: true,
  parseColors: parseColorOptions,
}

const customPublicCollections = importSvgCollections({
  source: path.resolve(packageDir, 'assets/public'),
  prefix: 'custom-public',
  ignoreImportErrors: true,
  ...svgOptimizeConfig,
})

const customVenderCollections = importSvgCollections({
  source: path.resolve(packageDir, 'assets/vender'),
  prefix: 'custom-vender',
  ignoreImportErrors: true,
  ...svgOptimizeConfig,
})

const source = `// Generated by packages/iconify-collections/scripts/generate-collections.mjs
// Do not edit manually.

import customPublicCollections from './custom-public.json' with { type: 'json' }
import customVenderCollections from './custom-vender.json' with { type: 'json' }

export {
  customPublicCollections,
  customVenderCollections,
}

export const svgCollections = {
  ...customPublicCollections,
  ...customVenderCollections,
}
`

await mkdir(generatedDir, { recursive: true })
await writeFile(path.resolve(generatedDir, 'custom-public.json'), `${JSON.stringify(customPublicCollections, null, 2)}\n`)
await writeFile(path.resolve(generatedDir, 'custom-vender.json'), `${JSON.stringify(customVenderCollections, null, 2)}\n`)
await writeFile(path.resolve(generatedDir, 'collections.js'), source)
