import { describe, expect, it } from 'vitest'
import { commandTree } from '@/commands/tree.generated'
import { renderSkill } from './skill'

const FILES = renderSkill(commandTree, { version: '9.9.9-test' })

function file(path: string): string {
  const found = FILES.find(f => f.path === path)
  if (!found)
    throw new Error(`skill file not found: ${path}`)
  return found.content
}

describe('renderSkill', () => {
  it('emits SKILL.md plus the three reference topics', () => {
    expect(FILES.map(f => f.path)).toEqual([
      'SKILL.md',
      'reference/account.md',
      'reference/external.md',
      'reference/environment.md',
    ])
  })

  it('stamps every file with the generated notice', () => {
    for (const f of FILES)
      expect(f.content).toContain('@generated by scripts/generate-skill.ts')
  })

  it('records the generated version and tells the agent to self-verify', () => {
    const s = file('SKILL.md')
    expect(s).toContain('difyctl v9.9.9-test')
    expect(s).toContain('`difyctl version`')
  })

  it('points at the machine-readable command surface rather than freezing it', () => {
    expect(file('SKILL.md')).toContain('difyctl help -o json')
  })

  it('inlines the contract: formats, exit codes, errors and HITL', () => {
    const s = file('SKILL.md')
    expect(s).toContain('OUTPUT FORMATS')
    expect(s).toContain('EXIT CODES')
    expect(s).toContain('ERRORS')
    expect(s).toContain('HUMAN-IN-THE-LOOP')
  })

  it('lists write and destructive commands in safety, excluding read-only ones', () => {
    const s = file('SKILL.md')
    const safety = s.slice(s.indexOf('## Safety'), s.indexOf('## Core workflow'))
    expect(safety).toContain('write — side effects')
    expect(safety).toContain('destructive — irreversible')
    expect(safety).toContain('run app')
    expect(safety).toContain('delete member')
    expect(safety).toContain('auth devices revoke')
    // read-only commands are safe to call freely and must stay unlisted
    expect(safety).not.toContain('get app')
    expect(safety).not.toContain('version')
  })

  it('renders the golden-path workflow in order, with HITL resume', () => {
    const s = file('SKILL.md')
    const wf = s.slice(s.indexOf('## Core workflow'))
    let idx = -1
    for (const step of ['auth login', 'get app', 'describe app', 'run app', 'resume app']) {
      const at = wf.indexOf(`difyctl ${step}`, idx + 1)
      expect(at, `step "${step}" out of order`).toBeGreaterThan(idx)
      idx = at
    }
  })

  it('embeds the topic bodies in the reference files', () => {
    expect(file('reference/account.md')).toContain('account-bearer onboarding')
    expect(file('reference/external.md')).toContain('dfoe_')
    expect(file('reference/environment.md')).toContain('ENVIRONMENT VARIABLES')
  })

  it('matches the committed snapshot', () => {
    expect(FILES).toMatchSnapshot()
  })
})
