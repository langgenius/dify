import type { HelpTopic } from './topics'
import type { CommandDescriptor } from '@/framework/help'
import type { CommandTree } from '@/framework/registry'
import { describeCommand } from '@/framework/help'
import { collectCommands, resolveCommand } from '@/framework/registry'
import { CONTRACT } from './contract'
import { SKILL_SHELL } from './skill-template'
import { findTopic } from './topics'

const BIN = 'difyctl'

const GENERATED_NOTICE = `<!-- @generated by scripts/generate-skill.ts — DO NOT EDIT.
     Regenerate via \`pnpm skill:gen\`; drift gated by \`pnpm skill:check\`. -->`

// Topics that ship as on-demand reference files. The `agent` topic is folded
// into SKILL.md's contract instead, so it is deliberately excluded here.
const REFERENCE_TOPICS = ['account', 'external', 'environment'] as const

export type SkillFile = {
  readonly path: string
  readonly content: string
}

export type RenderSkillOptions = {
  readonly version: string
}

function pad(s: string, width: number): string {
  return s.length >= width ? s : s + ' '.repeat(width - s.length)
}

function describe(tree: CommandTree, path: string): CommandDescriptor {
  const resolved = resolveCommand(tree, path.split(' '))
  if (!resolved)
    throw new Error(`skill workflow references unknown command: ${path}`)
  return describeCommand(resolved.command, resolved.path.join(' '))
}

function contractBlock(): string {
  const formats = CONTRACT.outputFormats.join(', ')

  const exitWidth = Math.max(...Object.keys(CONTRACT.exitCodes).map(c => c.length))
  const exitCodes = Object.entries(CONTRACT.exitCodes)
    .map(([code, desc]) => `  ${pad(code, exitWidth)}  ${desc}`)
    .join('\n')

  return `## Contract

OUTPUT FORMATS
  ${formats}

EXIT CODES
${exitCodes}

ERRORS
  ${CONTRACT.errorEnvelope.description}
    ${CONTRACT.errorEnvelope.shape}

HUMAN-IN-THE-LOOP
  ${CONTRACT.hitl.description}
  Resume: ${CONTRACT.hitl.resume}`
}

function safetyBlock(tree: CommandTree): string {
  const byEffect = (effect: string) =>
    collectCommands(tree)
      .map(({ command, path }) => describeCommand(command, path.join(' ')))
      .filter(d => d.effect === effect)
      .sort((a, b) => (a.command < b.command ? -1 : a.command > b.command ? 1 : 0))

  const write = byEffect('write')
  const destructive = byEffect('destructive')
  const width = Math.max(...[...write, ...destructive].map(d => d.command.length), 0)

  const list = (items: CommandDescriptor[]) =>
    items.map(d => `  ${pad(d.command, width)}  ${d.description ?? ''}`).join('\n')

  return `## Safety

${SKILL_SHELL.safetyFraming}

write — side effects, mostly recoverable:
${list(write)}

destructive — irreversible / data loss:
${list(destructive)}`
}

function workflowBlock(tree: CommandTree): string {
  const steps = SKILL_SHELL.workflow.map((path, i) => {
    const d = describe(tree, path)
    const example = d.examples[0]
    const lines = [`${i + 1}. ${BIN} ${d.command}  — ${d.description ?? ''}`]
    if (example !== undefined)
      lines.push(`     $ ${example}`)
    return lines.join('\n')
  })

  return `## Core workflow

${steps.join('\n\n')}

If \`${BIN} run app\` exits 2 the workflow paused for human input (see
HUMAN-IN-THE-LOOP above); resume it with \`${BIN} resume app\`.`
}

function referenceBlock(): string {
  const width = Math.max(...REFERENCE_TOPICS.map(n => `reference/${n}.md`.length))
  const rows = REFERENCE_TOPICS.map((name) => {
    const topic = findTopic(name)
    const summary = topic?.summary ?? ''
    return `  ${pad(`reference/${name}.md`, width)}  ${summary}`
  }).join('\n')

  return `## Reference

Long-form topics, loaded on demand:
${rows}`
}

function renderSkillMd(tree: CommandTree, opts: RenderSkillOptions): string {
  return `---
name: ${SKILL_SHELL.name}
description: ${SKILL_SHELL.description}
---

${GENERATED_NOTICE}

${SKILL_SHELL.opening}

Generated against ${BIN} v${opts.version}. Run \`${BIN} version\` to confirm your
binary matches; if it differs, refetch or regenerate this skill.

## Discovering the full command surface

This file does not freeze the full command set. Fetch it, machine-readable, in
one call:

    ${BIN} help -o json

That returns every command (description, args, flags, examples, agentGuide,
effect) plus the contract below.

${contractBlock()}

${safetyBlock(tree)}

${workflowBlock(tree)}

${referenceBlock()}
`
}

function renderReference(topic: HelpTopic): string {
  return `${GENERATED_NOTICE}

# ${topic.name}

> ${topic.summary}

${topic.render()}`
}

export function renderSkill(tree: CommandTree, opts: RenderSkillOptions): SkillFile[] {
  const files: SkillFile[] = [
    { path: 'SKILL.md', content: renderSkillMd(tree, opts) },
  ]

  for (const name of REFERENCE_TOPICS) {
    const topic = findTopic(name)
    if (!topic)
      throw new Error(`skill reference references unknown topic: ${name}`)
    files.push({ path: `reference/${name}.md`, content: renderReference(topic) })
  }

  return files
}
